const zhToEn = new Map(literalPairs.map(([zh, en]) => [zh, en]));
const enToZh = new Map(literalPairs.map(([zh, en]) => [en, zh]));

export function useI18n() {
  const { locale } = useAppState();
  const t = useCallback((key: TranslationKey) => translations[locale][key], [locale]);
  const tx = useCallback(
    (text: string) => {
      if (!text) return text;
      if (locale === "en") {
        if (zhToEn.has(text)) return zhToEn.get(text)!;
        let next = text;
        for (const [zh, en] of literalPairs) next = next.replaceAll(zh, en);
        return next;
      }
      if (enToZh.has(text)) return enToZh.get(text)!;
      let next = text;
      for (const [zh, en] of literalPairs) next = next.replaceAll(en, zh);
      return next;
    },
    [locale]
  );

  return { locale, isZh: locale === "zh-HK", t, tx };
}
